function getNextVersionFromCommitMessages(version, titles, bodies) {
  if (semver_39$1(version)) {
    return semver_8$1(version, 'prerelease');
  }
  if (bodies.toUpperCase().includes(GIT_COMMIT_BREAKING_CHANGE)) {
    return semver_8$1(version, 'major');
  }
  let patch = false;
  let minor = false;
  titles.split('\n').forEach(rawTitle => {
    const title = rawTitle.trim();
    if (!title) {
      return;
    }
    const match = title.match(/(.*?)(\(.*?\))?:.*/);
    if (!match || !match[1]) {
      throw new Error(`Invalid commit message format.\n  > ${title}`);
    }
    const prefix = match[1].toLowerCase();
    if (GIT_COMMIT_PREFIX_PATCH.has(prefix)) {
      patch = true;
    } else if (GIT_COMMIT_PREFIX_MINOR.has(prefix)) {
      minor = true;
    } else {
      throw new Error(`Out of convention.\n  > ${title}`);
    }
  });

  if (minor) {
    return semver_8$1(version, 'minor');
  } else if (patch) {
    return semver_8$1(version, 'patch');
  } else {
    return null;
  }
}

